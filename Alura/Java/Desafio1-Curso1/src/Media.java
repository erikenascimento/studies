/*
Crie um programa que realize a média de duas notas decimais e exiba o resultado.
 */

public class Media {
    public static void main(String[] args) {
        double nota1 = 7.5;
        double nota2 = 6.5;

        double media = (nota1 + nota2)/2;
        System.out.println("A média é de: " + media);
    }
}
